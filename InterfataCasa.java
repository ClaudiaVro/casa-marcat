    private void adaugaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adaugaActionPerformed
        String item = jList1.getSelectedValue(); // produs selectat din lista
        Produs[] produse = declaraObiecte(); // preia array-ul de obiecte
        for(int i = 0; i < produse.length; i++){ // compara Nume cu item ca sa preia CodNumar si booleanul buc/kg
            String Nume = getNumeProdus(i);
            if(Nume.equals(item)){
                if (produse[i].esteBuc){
                    CodNumar = produse[i].getCod();
                    adaugaProdus();
                    return;
                } else if (!produse[i].esteBuc && kg != 0 ){
                    CodNumar = produse[i].getCod();
                    adaugaProduskg();
                    return;
                } else { 
                    eroare("Niciun gramaj inregistrat.");
                }
            }
        }

    }//GEN-LAST:event_adaugaActionPerformed
  
    
