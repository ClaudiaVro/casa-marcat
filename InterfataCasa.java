    private void rIncaseazaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rIncaseazaActionPerformed
         if (rTotal.getText().equals(""))
        {
            eroare("Apasati TOTAL intai.");
            return;
        }
         String Suma = rNumerar.getText();
         String Total = rTotal.getText();
         double total = Double.parseDouble(Total);
         
         int suma = Integer.parseInt(Suma);
                 
         int parteIntreaga = suma / 100 ;
         double parteZecimala = suma % 100;
         double sumaDouble = (double) parteIntreaga + parteZecimala/100;
         Suma = Double.toString(sumaDouble);
         rNumerar.setText(Suma);
         
         if (sumaDouble <= total){  // daca suma data e mai mica ca si totalul
             rNumerar.setText(null);
             total = total - sumaDouble;
             Total = String.format("%.02f", total);
             rTotal.setText(Total);
         } else { // se da rest
             rTotal.setText(null);
             rNumerar.setText(null);
             LNume.setText(null);
             LCost.setText(null);
             LSubtotal.setText(null);
             double rest = sumaDouble - total;
             String Rest = Double.toString(rest);
             Rest = String.format("%.02f", rest); 
             rRest.setText(Rest);
             subTotal = 0 ;
             
             adaugaLista("delete","");
            
         }
    }//GEN-LAST:event_rIncaseazaActionPerformed
    
