    public void adaugaProduskg(){
        String produsVer = cautaObiecte(CodNumar, "Nume");  
        LNume.setText(kg + " x " + produsVer);
            
        String Pret = cautaObiecte(CodNumar, "Pret"); // pretul gasit din metoda(sau din obiect)
        double pret = Double.parseDouble(Pret);  // transforma pretul din obiect in double pentru a inmulti cu kilogramul introdus anterior
        double PretAfisat = kg * pret;  
        Pret = Double.toString(PretAfisat); // conversie inapoi la String pentru a fi afisat in box
        Pret = String.format("%.02f", PretAfisat);
        LCost.setText(Pret);
                
        subTotal += PretAfisat;
        String SubTotal = Double.toString(subTotal);
        SubTotal = String.format("%.02f", subTotal); 
        LSubtotal.setText(SubTotal);
                
        adaugaLista(produsVer, Pret);
        kg = 0;
    }  
    
    public void adaugaProdus(){
        counter = 0;
        String produsVer = cautaObiecte(CodNumar, "Nume");
        counter++;
        LNume.setText(counter +"x "+ produsVer);
        
        String Pret = cautaObiecte(CodNumar, "Pret");
        double pret = Double.parseDouble(Pret); // folosit mai tarziu pentru afisaj subtotal
        LCost.setText(Pret);
        
        subTotal += pret;
        String SubTotal = Double.toString(subTotal);
        SubTotal = String.format("%.02f", subTotal); 
        LSubtotal.setText(SubTotal);

        adaugaLista(produsVer, Pret);  
    }
    private void LButtonInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LButtonInputActionPerformed
        rRest.setText(null);
        if(TextIntrodus.getText().equals("")){ // verifica daca s-a tastat cv in TextIntrodus
            eroare("Niciun cod valid introdus.");
            return;
        }
        String codIntrodus = TextIntrodus.getText();
        if (!codIntrodus.contains("."))
            CodNumar = Integer.parseInt(codIntrodus);
        else { 
            eroare("Codul nu este valabil");
            TextIntrodus.setText(null);
            return;         
        }
        if (CodNumar > 9999 || CodNumar < 999){ // verifica daca CodProdus este valabil
            eroare("Codul nu este valabil.");
            TextIntrodus.setText(null);
            return;
        }
        
       String gasit = cautaObiecte(CodNumar,"Nume");
       if(gasit == null){
          eroare("Codul nu este valabil.");
          TextIntrodus.setText(null); 
          return;
       }       
        
        if(kg != 0){
            if(cautaObiecte(CodNumar,"Boolean").equals("false")){ // verifica daca produsul are atributul de produs la kilogram
                adaugaProduskg();
            } else { 
                eroare("Produsul este la bucata.");
            }   
        } else if (cautaObiecte(CodNumar,"Boolean").equals("true")){ // conditia de produs la bucata 
            adaugaProdus();
        } else   // conditie de produs la kilogram introdus fara prefixul de kilogram
            eroare("Produsul este la kilogram.");             
        TextIntrodus.setText(null);
        
    }//GEN-LAST:event_LButtonInputActionPerformed

    public void adaugaLista(String nume, String pret){
        DefaultTableModel model = (DefaultTableModel) Tabel.getModel(); 
        if(!nume.equals("delete")){
            model.addRow(new Object[]{nume,pret});
       } else { 
             int rowCount = model.getRowCount();
             for(int i = rowCount - 1; i >= 0 ; i--){
                 model.removeRow(i);
            }
        }       
}
 
