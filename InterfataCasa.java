    public String cautaObiecte(int cod, String ceCaut){
        
    Produs[] produse = declaraObiecte();
    
    String produsGasit = null;  // returneaza "atributul" produsului in functie de paraemtrul ceCaut
        switch (ceCaut) {
            case "Nume":
                for (Produs produse1 : produse) {
                    if (produse1.getCod() == cod) {
                        produsGasit = produse1.getNume();                    
                    }
                }       break;
            case "Pret":
                for (Produs produse1 : produse) {
                    if (produse1.getCod() == cod) {
                        double Pret = produse1.getPret();
                        produsGasit = String.format("%.02f", Pret);
                    }
                }       break;
            case "Boolean":
                for (Produs produse1 : produse) {
                    if (produse1.getCod() == cod) {
                        if (!produse1.esteBuc) {
                            produsGasit = "false";
                        } else {
                            produsGasit = "true";
                        }
                    }
                }       break;            
        }
    return produsGasit;
}
   
